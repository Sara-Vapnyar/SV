int	ft_iterative_factorial(int nb)
{
	int		i;
	int		res;

	i = 1;
	res = 1;
	if (nb >= 0 && nb < 13)
	{
		while (i <= nb)
		{
			res = i * res;
			i++;
		}
		return (res);
	}
	else
	{
		return (0);
	}
}
